#!/usr/bin/env node
/**
 * Generates src/data/siteMap.json — the data behind the /site-map page.
 * Runs automatically before `docusaurus start` and `docusaurus build`.
 *
 * The script:
 *   1. Parses sidebars.ts to learn which audience sidebars include which directories.
 *   2. Walks docs/ and extracts frontmatter (title, displayed_sidebar) from every page.
 *   3. Groups pages by directory; tags each directory with its audiences and primary owner.
 *   4. Writes a JSON manifest consumed by src/components/SiteMap.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DOCS_DIR = path.join(ROOT, 'docs');
const SIDEBARS_FILE = path.join(ROOT, 'sidebars.ts');
const OUT_FILE = path.join(ROOT, 'src', 'data', 'siteMap.json');

const AUDIENCE_LABEL = {
  userSidebar: 'Users',
  developerSidebar: 'Developers',
  deploymentSidebar: 'Deployment',
};

const AUDIENCE_ORDER = ['userSidebar', 'developerSidebar', 'deploymentSidebar'];

// ── 1. Parse sidebars.ts to map sidebar → [directories] ──────────────
function parseSidebars() {
  const src = fs.readFileSync(SIDEBARS_FILE, 'utf8');
  const sidebars = {};

  // Match each top-level sidebar key: e.g. `userSidebar: [ ... ],`
  for (const sidebarKey of Object.keys(AUDIENCE_LABEL)) {
    const start = src.indexOf(`${sidebarKey}:`);
    if (start === -1) continue;

    // Walk forward and find the balanced [ ... ] block
    const openIdx = src.indexOf('[', start);
    let depth = 0;
    let endIdx = -1;
    for (let i = openIdx; i < src.length; i++) {
      if (src[i] === '[') depth++;
      else if (src[i] === ']') {
        depth--;
        if (depth === 0) { endIdx = i; break; }
      }
    }
    if (endIdx === -1) continue;

    const block = src.slice(openIdx, endIdx + 1);

    // Capture autogenerated dirNames
    const dirs = [...block.matchAll(/dirName:\s*['"]([^'"]+)['"]/g)].map(m => m[1]);

    // Capture top-level doc IDs (string entries like 'introduction')
    // These are loose strings inside the sidebar, e.g. 'introduction', 'changelog', etc.
    const looseDocs = [...block.matchAll(/^\s*['"]([\w-]+(?:\/[\w-]+)?)['"],?\s*$/gm)].map(m => m[1]);

    sidebars[sidebarKey] = { dirs, looseDocs };
  }

  return sidebars;
}

// ── 2. Walk docs/ and extract frontmatter ─────────────────────────────
function walkDocs(dir, base = '') {
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    const rel = base ? `${base}/${entry.name}` : entry.name;
    if (entry.isDirectory()) {
      out.push(...walkDocs(full, rel));
    } else if (/\.(md|mdx)$/.test(entry.name)) {
      out.push(rel);
    }
  }
  return out;
}

function parseFrontmatter(filePath) {
  const src = fs.readFileSync(filePath, 'utf8');
  const match = src.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return {};
  const fm = {};
  for (const line of match[1].split(/\r?\n/)) {
    const m = line.match(/^([\w-]+):\s*(.*)$/);
    if (!m) continue;
    let [, key, value] = m;
    value = value.trim().replace(/^["']|["']$/g, '');
    fm[key] = value;
  }
  return fm;
}

function deriveTitle(filePath, fm) {
  if (fm.title) return fm.title;
  const src = fs.readFileSync(filePath, 'utf8');
  const h1 = src.match(/^#\s+(.+?)\s*$/m);
  if (h1) return h1[1];
  // fall back to filename
  return path.basename(filePath).replace(/\.(md|mdx)$/, '');
}

// ── 3. Build the manifest ─────────────────────────────────────────────
function build() {
  const sidebars = parseSidebars();

  // Build dir → [audiences] map
  const dirAudiences = {}; // dirName → Set<sidebarKey>
  for (const [sb, { dirs }] of Object.entries(sidebars)) {
    for (const d of dirs) {
      (dirAudiences[d] ??= new Set()).add(sb);
    }
  }

  // Walk every doc page
  const allDocs = walkDocs(DOCS_DIR);
  const dirPages = {}; // dirName → [{ id, title, primary, slug }]
  const rootPages = []; // top-level docs not in any subdirectory

  for (const rel of allDocs) {
    const filePath = path.join(DOCS_DIR, rel);
    const fm = parseFrontmatter(filePath);
    const title = deriveTitle(filePath, fm);
    const idNoExt = rel.replace(/\.(md|mdx)$/, '');
    const parts = idNoExt.split('/');
    const dir = parts.length > 1 ? parts[0] : null;
    // Docusaurus treats `index` as the directory's root URL.
    const slugId = idNoExt.replace(/\/index$/, '');
    const slug = `/docs/${slugId}`;
    const page = {
      id: idNoExt,
      title,
      slug,
      primary: fm.displayed_sidebar || null,
    };

    if (dir) {
      (dirPages[dir] ??= []).push(page);
    } else {
      rootPages.push(page);
    }
  }

  // Sort pages within each directory by sidebar_position when available, else title
  for (const dir of Object.keys(dirPages)) {
    dirPages[dir].sort((a, b) => a.title.localeCompare(b.title));
  }

  // Compose audience columns. Each audience lists:
  //   - root docs that appear in its sidebar (e.g. 'introduction' for userSidebar)
  //   - directories whose primary audience is this one
  // Shared directories are listed in their primary audience but tagged with `sharedWith`.
  const audiences = AUDIENCE_ORDER.map(sb => ({
    key: sb,
    label: AUDIENCE_LABEL[sb],
    rootDocs: [],
    dirs: [],
  }));

  // Place root docs by which sidebar(s) lists them
  for (const sb of AUDIENCE_ORDER) {
    const ids = sidebars[sb]?.looseDocs || [];
    const aud = audiences.find(a => a.key === sb);
    for (const id of ids) {
      const doc = rootPages.find(p => p.id === id);
      if (doc) aud.rootDocs.push(doc);
    }
  }

  // Place directories by primary audience.
  // Primary audience = first sidebar in AUDIENCE_ORDER that contains the dir,
  // OR — preferred — the displayed_sidebar of the dir's pages if consistent.
  for (const [dir, pages] of Object.entries(dirPages)) {
    const audSet = dirAudiences[dir];
    if (!audSet || audSet.size === 0) continue; // dir not in any sidebar (e.g. templates)

    // Determine primary: most common displayed_sidebar among pages, else first sidebar in order
    const primaryCounts = {};
    for (const p of pages) {
      if (p.primary) primaryCounts[p.primary] = (primaryCounts[p.primary] || 0) + 1;
    }
    let primary;
    if (Object.keys(primaryCounts).length > 0) {
      primary = Object.entries(primaryCounts).sort((a, b) => b[1] - a[1])[0][0];
    } else {
      primary = AUDIENCE_ORDER.find(sb => audSet.has(sb));
    }

    const sharedWith = [...audSet].filter(sb => sb !== primary);

    const dirEntry = {
      name: dir,
      label: prettyLabel(dir),
      pages,
      primary,
      sharedWith,
    };

    const aud = audiences.find(a => a.key === primary);
    if (aud) aud.dirs.push(dirEntry);
  }

  // Sort dirs within each audience by label
  for (const aud of audiences) {
    aud.dirs.sort((a, b) => a.label.localeCompare(b.label));
  }

  return {
    generatedAt: new Date().toISOString(),
    audiences,
  };
}

function prettyLabel(dir) {
  // 'getting-started' → 'Getting Started'
  // 'api' → 'API Reference' (special case)
  if (dir === 'api') return 'API Reference';
  return dir
    .split(/[-_]/)
    .map(w => w[0].toUpperCase() + w.slice(1))
    .join(' ');
}

// ── 4. Write output ───────────────────────────────────────────────────
const manifest = build();
fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, JSON.stringify(manifest, null, 2));
console.log(`✓ Site map written to ${path.relative(ROOT, OUT_FILE)} (${manifest.audiences.reduce((n, a) => n + a.dirs.length + a.rootDocs.length, 0)} entries)`);
